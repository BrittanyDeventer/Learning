<p>
This site allows users to input multiple destinations into a form, and, once submitted, see their entire road trip laid out on the map.
My inspiration came from the map sequences in the Indiana Jones movies, and my motivation came from many months out on the road with a broadway show.  
I was constantly wishing I could access a map that had our entire 7 month route mapped out in a way that I could see where we'd be going next and where 
we would be coming from.  Hopefully this helps other people on their trips!  
</p>
<h2> Getting Started </h2>
<p>
Click on "Add a Trip" to begin.  Enter as many destinations as you like! 
Once submitted, you should be taken to the map page where your trip will be laid out for you!  
Unfortunately, this site is limited to trips within the US.
Enjoy!
</p>
